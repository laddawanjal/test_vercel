const stream = require("stream");

async function uploadFile(folderId, base64Image, fileName) {
  try {
    // ‡πÅ‡∏õ‡∏•‡∏á Base64 ‡πÄ‡∏õ‡πá‡∏ô Buffer
    const buffer = Buffer.from(base64Image.replace(/^data:image\/\w+;base64,/, ""), "base64");

    // ‡∏™‡∏£‡πâ‡∏≤‡∏á Stream ‡∏™‡∏≥‡∏´‡∏£‡∏±‡∏ö‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡πÑ‡∏ü‡∏•‡πå‡πÇ‡∏î‡∏¢‡∏ï‡∏£‡∏á
    const bufferStream = new stream.PassThrough();
    bufferStream.end(buffer);

    // ‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡πÑ‡∏õ‡∏¢‡∏±‡∏á Google Drive
    const fileMetadata = {
      name: fileName,
      parents: [folderId],
    };

    const media = {
      mimeType: "image/jpeg",
      body: bufferStream,
    };

    const file = await drive.files.create({
      resource: fileMetadata,
      media: media,
      fields: "id",
    });

    if (!file.data.id) {
      console.error("‚ùå Upload failed: No file ID returned.");
      return null;
    }

    console.log(`‚úÖ Upload Success: ${fileName} (File ID: ${file.data.id})`);
    return file.data.id;
  } catch (error) {
    console.error("‚ùå Error uploading file:", error.message);
    return null;
  }
}








